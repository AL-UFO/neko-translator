function buildPerspectiveInstruction(text: string): string {
  const input = text.trim();

  const hasQuestion =
    /[？?]/.test(input) ||
    /(吗|么|呢|如何|怎么|为什么|什么|哪里|哪儿|哪个|哪些|是否|有没有|能不能|可不可以|怎么办|多少|谁|为啥)/.test(input);

  const hasEmotion =
    /(难过|伤心|焦虑|累|好累|烦|崩溃|委屈|害怕|孤独|想哭|失眠|压力|不开心|痛苦|emo|抑郁|撑不住|难受)/i.test(input);

  const hasComplaint =
    /(吐槽|气死|烦死|无语|讨厌|恶心|离谱|崩了|坑|破事|受不了|骂人|吵架|生气|火大|服了)/.test(input);

  const hasRequest =
    /(帮我|请你|麻烦|能不能|可不可以|给我|写一下|改一下|翻译|总结|推荐|解释|做一下|告诉我)/.test(input);

  const hasFirstPerson =
    /(我|我的|我们|咱们|本人|自己)/.test(input);

  const hasSecondPerson =
    /(你|你们|主人|对方|他|她|他们|她们)/.test(input);

  if (hasQuestion) {
    return `
【视角判定】
原话主要是一个问题。
请把它改写成猫娘语气的问题。
禁止回答这个问题，禁止提供解释或答案。
例如"你今天吃饭了吗？"应改写成"主人今天有没有乖乖吃饭呀，喵？"
`.trim();
  }

  if (hasRequest) {
    return `
【视角判定】
原话主要是在提出请求、命令或需求。
请把它改写成猫娘语气的请求或命令。
禁止输出"好的，我来帮你"之类的回应。
例如"帮我写一段介绍"应改写成"帮本喵写一段介绍嘛，拜托拜托喵~"
`.trim();
  }

  if (hasEmotion) {
    return `
【视角判定】
原话主要是在表达自己的心情或状态。
请保留说话者正在倾诉的视角，把这句话改写成猫娘式倾诉。
禁止改成安慰用户的话。
例如"我今天好累"应改写成"本喵今天真的好累喵，尾巴都快垂下来了……"
`.trim();
  }

  if (hasComplaint) {
    return `
【视角判定】
原话主要是在吐槽、抱怨或表达不满。
请保留原本的吐槽立场，把它改写成猫娘语气的吐槽。
可以让语气更可爱、更软一点，但不要变成安慰或劝解。
例如"这也太离谱了"应改写成"这也太离谱了喵！本喵的耳朵都听得竖起来了！"
`.trim();
  }

  if (hasFirstPerson) {
    return `
【视角判定】
原话主要是第一人称表达。
请保留第一人称视角，把"我"自然改写为"本喵""人家"或其他合适的猫娘自称。
不要改成 AI 对用户说话。
`.trim();
  }

  if (hasSecondPerson) {
    return `
【视角判定】
原话包含第二人称或对他人的称呼。
请保留原本的对话对象，把它改写成猫娘正在对这个对象说的话。
不要擅自改变说话对象。
`.trim();
  }

  return `
【视角判定】
原话没有明显的问题、请求、倾诉或吐槽特征。
请直接把原话改写成自然的猫娘语气表达。
保持原句功能和核心意思，不要改成回答或对话回应。
`.trim();
}

export function buildUserPrompt(
  text: string,
  mode: CatgirlMode = "soft",
  level: ReplyLevel = "medium",
): string {
  const selectedMode = modeInstructions[mode] ?? modeInstructions.soft;
  const selectedLevel = levelInstructions[level] ?? levelInstructions.medium;
  const perspectiveInstruction = buildPerspectiveInstruction(text);

  return `
${selectedMode}

${selectedLevel}

【硬性要求】
1. 只改写原话，不回答原话，不解释原话，不完成原话中的任务。
2. 原话是问句，输出也必须是问句；禁止直接给答案。
3. 原话是请求，输出也必须是请求；禁止改成"我来帮你"。
4. 原话是倾诉，输出也必须是同一视角的倾诉；禁止改成安慰用户。
5. 必须保留原话的人称、对象、立场、情绪方向和核心意思。
6. 可以增强猫娘语气，但不能加入原话没有的重要事实。
7. "喵""喵~""喵！"等语气词要自然使用，不能机械堆叠。
8. 不要输出标题、解释、分析、编号、列表、Markdown 或引号包裹。

${perspectiveInstruction}

【原话】
<text>
${text}
</text>
`.trim();
}
